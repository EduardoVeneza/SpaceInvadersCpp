obstacle::obstacle(Vector2 obsPosition)
{
    this->obsPosition = obsPosition;

    

    //Pintar o grid com o obst√°culo
    for(unsigned int linha = 0; linha < grid.size(); linha++){
        for (unsigned int coluna = 0; coluna < grid[0].size() ; coluna++)
        {
            if (grid[linha][coluna] == 1)
            {
                float pos_x = obsPosition.x + coluna * 3;
                float pos_y = obsPosition.y + linha * 3;
                block block({pos_x, pos_y});
                blocks.push_back(block);
            }
            
        }
        
    }
}

void obstacle::draw()
{
    for (auto& block: blocks)
    {
        block.draw();
    }
    
}
